               <div id="zagl">Системы автономного энергоснабжения</div>
<br>
<b>НПО "Транстехника"</b> предлагает системы резервного или автономного энергообеспечения на базе 
инверторов фирмы <b>Victron Energy</b>.
<br>&nbsp;&nbsp;Современная электронная система <b>обеспечит надежную и бесперебойную работу</b> систем безопасности, 
охранной и пожарной сигнализации, систем автономного отопления, АТС и факсимильной связи, гарантирует 
информационную безопасность не только центрального сервера, но и, в отличие от UPS, целой информационной 
сети, включая все виды осветительного и электротехнического оборудования, даст электричество Вашему дому,
 офису, клинике, обменному пункту, лаборатории - там, где перебои с электричеством приравниваются к 
 катастрофе.
<br>&nbsp;&nbsp;В состав системы входит <b>инвертор компании Victron Energy</b> и <b>комплект аккумуляторов</b> глубокого цикла зарядки.
<br>
<br>&nbsp;&nbsp;<b>Достоинства системы:</b>
<br>&nbsp;&nbsp;&nbsp;&nbsp;•	проста и надежна;
<br>&nbsp;&nbsp;&nbsp;&nbsp;•	имеет малую потребляемую мощность - не более16 Вт (1 Вт в режиме ожидания);
<br>&nbsp;&nbsp;&nbsp;&nbsp;•	бесшумна;
<br>&nbsp;&nbsp;&nbsp;&nbsp;•	эффективна (КПД - до 96%)
<br>&nbsp;&nbsp;&nbsp;&nbsp;•	малогабаритна;
<br>&nbsp;&nbsp;&nbsp;&nbsp;•	работает практически с ЛЮБЫМ электрооборудованием (включая электромоторы, 
холодильники, телевизоры, компьютеры, лазерные принтеры, кассовые аппараты и т.д.)

<br><br>&nbsp;&nbsp;
В режиме ожидания <b>инвертор</b> пропускает имеющееся напряжение электросети на выход и одновременно 
подзаряжает и поддерживает в рабочем состоянии аккумуляторы мощным встроенным <b>зарядным устройством</b>. 
В момент отключения или снижения ниже установленного значения напряжения электросети система практически 
мгновенно (16 мсек) переключается на работу от аккумуляторов и преобразует их энергию в переменный ток 
стабилизированного напряжения 220В/50Гц. При появлении электричества система, проанализировав качество 
поступающего напряжения, <b>автоматически</b> переходит в режим заряда или продолжает работать в режиме 
преобразования (если параметры сети не соответствуют установленным) до восстановления нормального 
напряжения в электросети.

<br><br>&nbsp;&nbsp;
Система позволяет работать с пусковыми нагрузками в <b>3-4</b> раза превышающими ее номинальную мощность, что 
необходимо для запуска <b>электромоторов</b>, <b>холодильников</b>, <b>насосов</b> и пр.

<br><br>&nbsp;&nbsp;
Система полностью <b>адаптирована для работы с альтернативными источниками энергии</b> (солнечными батареями, 
ветро- и гидрогенераторами малой мощности). Подключение к альтернативному источнику производится через 
соответствующий <b>контроллер</b> производства компании <b>Xantrex</b>.

<br><br>&nbsp;&nbsp;
Совместно с <b>генератором</b>, работающим на жидком топливе, который может быть установлен дополнительно, 
возможно создание <b>автономной системы</b>. В этом случае <b>инвертор серии SW</b>, самостоятельно управляя работой 
<b>генератора</b> (автоматическое включение и выключение по заданной программе через отдельное меню и систему 
таймеров или других альтернативных источников), позволяет создать <b>собственную автономную электросеть</b>.
<br><br>&nbsp;&nbsp;
Все преобразователи <b>Victron Energy</b> имеют много уровней защиты и <b>специальные устройства</b> для предохранения систем 
от электрических перегрузок, коротких замыканий. И, наконец, <b>зарядные устройства</b> преобразователя 
обеспечивают современный трехступенчатый процесс заряда с температурной компенсацией, что не позволяет 
аккумуляторам "кипеть", предохраняет аккумуляторные батареи от полного разряда и обеспечивает 
максимальную эффективность и продолжительность срока службы аккумулятора.<br><br>
