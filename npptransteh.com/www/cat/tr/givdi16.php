Присоединение заземления береговой сети непосредственно к металлическому корпусу приводит к тому, что для всех судов в гавани береговое заземление становится общим. При этом, к сожалению, между судами появляется гальваническая связь, которая может вызвать у некоторых корпусов серьезную коррозию. Также очень быстро будут разрушаться защитные аноды судов. Гальванический изолятор позволяет сохранить связь с береговой "землей" (и тем самым обеспечивает безопасность жизни), но не дает всем маленьким блуждающим токам попадать на судно или выходить из него (и тем самым сохраняет корпус судно от разрушения).
