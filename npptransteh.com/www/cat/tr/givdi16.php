<h1>Установка изолятора</h1>

Гальванический изолятор выделяет большое количество тепла, поэтому в целях безопасности он должен устанавливаться в хорошо проветриваемом месте. Избегайте присутствия вокруг изолятора химикатов, синтетических материалов, занавесок или другого текстиля и т.п.
<br><br>
